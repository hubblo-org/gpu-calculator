	<h2 id="introduction-title">GPU embedded impacts calculator</h2>
	<p>
		This calculator estimates the embedded impacts (manufacturing, distribution, end-of-life) of a graphics card based on 16 Product Environmental Footprint (PEF) impact criteria (excluding the use phase). It is based on the parametric model developed for <a href="https://ademe.fr"><span>ADEME</span></a> by <a href="https://www.linkedin.com/company/tide-env/"><span>Tide</span></a> and <a href="https://hubblo.org/fr/"><span>Hubblo</span></a> as part of research published in 2025 <a href="https://arxiv.org/abs/2509.00093"><span>(Falk et al., 2025)</span></a>. The model is the result of extensive data collection, including the dismantling of a dozen graphics cards and the performance of a dozen elemental analyses.
	</p>

